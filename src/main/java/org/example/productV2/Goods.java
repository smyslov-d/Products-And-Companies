package org.example.productV2;

public class Goods {
    int id;
    String name;
    int comp_id;
    int price;

    public Goods() {

    }

    public Goods(int id, String name, int comp_id, int price) {
        this.id = id;
        this.name = name;
        this.comp_id = comp_id;
        this.price = price;
    }
}
